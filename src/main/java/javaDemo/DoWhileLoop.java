package javaDemo;

public class DoWhileLoop {

	public static void main(String[] args) {

int i=1;
do {
	System.out.println(i);
	i= i+3;
}while(i<=15);
//it will print 1 to 15 using do while loop
// if you dont use increment (i=i++) in while or do while loop then it will run infinite times that it never end

	}
	

}